package br.pokemon.com;

public class HabilidadePokemon {
	private String codHabilidade;
	private String codPokemon;
	
	public String getCodHabilidade() {
		return codHabilidade;
	}
	public void setCodHabilidade(String codHabilidade) {
		this.codHabilidade = codHabilidade;
	}
	public String getCodPokemon() {
		return codPokemon;
	}
	public void setCodPokemon(String codPokemon) {
		this.codPokemon = codPokemon;
	}
}